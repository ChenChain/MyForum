    /**
     * 发送短信验证码
     *
     * @param mobile 手机号
     */
    public void sendSms(String mobile) {

        Random random = new Random();
        int max = 999999;//最大数
        int min = 100000;//最小数
        int code = random.nextInt(max);//随机生成
        if (code < min) {
            code = code + min;
        }
        System.out.println(mobile + "收到验证码是：" + code);

        redisTemplate.opsForValue().set("smscode_" + mobile, code + "", 5,
                TimeUnit.MINUTES);//五分钟过期
        Map<String, String> map = new HashMap();
        map.put("mobile", mobile);
        map.put("code", code + "");
        rabbitTemplate.convertAndSend("sms", map);
    }

    /**
     * 用户注册 需要手机号验证
     *
     * @param user
     * @param code
     */
    public void add(User user, String code) {
        String mecode = (String) redisTemplate.opsForValue().get("smscode_" + user.getMobile());
        if (mecode == null) {
            throw new RuntimeException("点击获取验证码");
        }
        if (!mecode.equals(code)) {
            throw new RuntimeException("验证码不正确");
        }
        user.setPassword(bCryptPasswordEncoder.encode(user.getPassword()));
        user.setId(idWorker.nextId() + "");
        user.setFollowcount(0);//关注数
        user.setFanscount(0);//粉丝数
        user.setOnline(0L);//在线时长
        user.setRegdate(new Date());//注册日期
        user.setUpdatedate(new Date());//更新日期
        user.setLastdate(new Date());//最后登陆日期
        userDao.save(user);

    }


